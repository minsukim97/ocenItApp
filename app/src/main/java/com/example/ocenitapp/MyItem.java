package com.example.ocenitapp;

public class MyItem {

    private String task_category;
    private String task_name;
    private String research_period;
    private String affiliation;
    private String research_director;
    private String support;

    public String getTask_category() {
        return task_category;
    }

    public void setTask_category(String task_category) {
        this.task_category = task_category;
    }

    public String getTask_name() {
        return task_name;
    }

    public void setTask_name(String task_name) {
        this.task_name = task_name;
    }

    public String getResearch_period() {
        return research_period;
    }

    public void setResearch_period(String research_period) { this.research_period = research_period; }

    public String getAffiliation() {
        return affiliation;
    }

    public void setAffiliation(String affiliation) {
        this.affiliation = affiliation;
    }

    public String getResearch_director() {
        return research_director;
    }

    public void setResearch_director(String research_director) {
        this.research_director = research_director;
    }
    public String getsupport() {
        return support;
    }

    public void setsupport(String support) { this.support = support; }

}